#!/usr/bin/env python3
"""
This module contains a type-annotated function floor which takes a float n
as an argument and returns the floor of the float.

Functions:
    floor(n: float) -> int:
        Returns the floor of the float n.
"""


def floor(n: float) -> int:
    """
    Returns the floor of the float n.

    Args:
        n (float): The float number to floor.

    Returns:
        int: The floor of the float n.
    """
    return int(n)
