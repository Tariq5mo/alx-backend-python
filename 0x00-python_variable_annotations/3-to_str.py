#!/usr/bin/env python3
"""
This module contains a type-annotated function to_str that takes a float n
as an argument and returns the string representation of the float.

Functions:
    to_str(n: float) -> str:
        Returns the string representation of the float n.
"""


def to_str(n: float) -> str:
    """
    Returns the string representation of the float n.

    Args:
        n (float): The float number to convert to a string.

    Returns:
        str: The string representation of the float n.
    """
    return str(n)
